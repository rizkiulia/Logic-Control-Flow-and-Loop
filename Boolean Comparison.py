# Contoh multiple comparison menggunakan Boolean Comparison 'and'
result = (3 >= 5) and (6 == 4)
print(result)

# Contoh multiple comparison menggunakan Boolean Comparison 'or'
result = (4 != 5) or (2 < 1)
print(result)

# Contoh multiple comparison menggunakan Comparison 'not'
result = not (5 + 5 == 10)
print(result)
